function MouseEnter(x) {
    x.style.textDecoration = 'underline';
    x.style.textDecorationColor = 'yellow';
    x.style.color = 'yellow';
}
function MouseLeave(x) {
    x.style.textDecoration = 'none';
    x.style.textDecorationColor = 'none';
    x.style.color = 'white';
}
